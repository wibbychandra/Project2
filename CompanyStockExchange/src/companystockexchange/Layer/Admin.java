    private void tb_listMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_listMouseClicked
        // TODO add your handling code here:
        try{
            String id = helper.getValueRows(tb_list, 0);
            String name = helper.getValueRows(tb_list, 1);
            String date = helper.getValueRows(tb_list, 2);
            String sheet = helper.getValueRows(tb_list, 3);
            String percent = helper.getValueRows(tb_list, 4);
            String price = helper.getValueRows(tb_list, 5);
            
            btn_updatedata.setEnabled(true);
            btn_mengahpusdata.setEnabled(true);
            
            this.product_id = id;
            tf_namaadmin.setText(name);
            tf_perlembaradmin.setText(sheet);
            dp_tanggaladmin.setDate(new Date(helper.parseStringToDatepickerFormat(date)));
            tf_persenadmin.setText(percent);
            tf_hargajualadmin.setText(price);
        }
        catch (Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tb_listMouseClicked

