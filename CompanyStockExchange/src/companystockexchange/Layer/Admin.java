    private void btn_tambahdataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahdataActionPerformed
        // TODO add your handling code here:
        try{
            String name = tf_namaadmin.getText();
            Date date = dp_tanggaladmin.getDate();
            String sheet = tf_perlembaradmin.getText();
            String percent = tf_persenadmin.getText();
            String price = tf_hargajualadmin.getText();
            
            model.setName(name);
            model.setTanggal(date);
            model.setLembar(sheet);
            model.setPersen(percent);
            model.setHarga(price);
            
            Boolean result = controller.create(model);
            if(result){
                JOptionPane.showMessageDialog(null, "Successfully Inserting Data");
            }
            else{
                JOptionPane.showMessageDialog(null, "Unsuccessfully Inserting Data");
            }
            clear();
            getAllData();
        }
        catch (Exception ex){
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_tambahdataActionPerformed

